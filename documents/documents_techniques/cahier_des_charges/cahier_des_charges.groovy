             Titre: Projet_Quideance.

             Nom de l’application : Quideance
 
Table des matières
Nom de l’application :  Quideance
L'application donnera à l"utilisateur la possibilité de répondre à un
questionnaire envoyé via un lien url.

L'espace de connexion il sera demandé :
    - un email
    - un mot de passe 

Le mot de passe :
    - il devra être réinialisé lors de la première connexion pour chaque admin 
    ou super admin crée
    - être composé d'au minimum 6 carractères, avec des chiffres  des lettres et caractères spéciaux
    - faire une fonction mot de passe oublié

Le questionnaire sera composé :
    - un titre
    - il devra être rempli dans un espace temps défini.
    Cet espace temps pourra être arrêté à tout moment mais 
    le formulaire restera visible.
    - un résumé descriptif du questionnaire.
    - sera composé d'une ou plusieurs questions.
    - le formulaire devra être rempli et envoyé de façon unique

Les réponses du formulaire pourront être de différent type :
    - une zone de texte aréa pour écrire librement
    - des boutons radio (réponse en booléen)
    - choix multiple

Il y aura deux types d'aministrateurs pour gérer le site.
L'admin pourra :
    - se connecter 
    - se déconnecter
    - créer son questionnaire
    - modifier son questionnaire
    - supprimer son questionnaire
    - voir tous les questionnaires de l'application
    - modifier son mot de passe
    - envoyer un questionnaire via un lien url
    - faire le traitement des résultats

Le super admin aura les mêmes droits que l'admin et pourra en plus :
    - modifier tous les questionnaires
    - supprimer tous questionnaires
    - créer un utilisateur (profil admin ,super admin)
    - modifier un utilisateur (profil admin, super admin)
    - supprimer un utilisateur (profil admin, super admin)

        



