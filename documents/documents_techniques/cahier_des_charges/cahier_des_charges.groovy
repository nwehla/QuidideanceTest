             Titre: Projet_Quideance.

             Nom de l’application : Quideance
 
Table des matières
Nom de l’application :  Quideance
L'application donnera à l"utilisateur la possibilité de répondre à un
questionnaire ou un sondage envoyé via un lien url.

Le questionnaire:
    - Un titre
    - Il devra être rempli dans un espace temps défini.
    - Un résumé descriptif du questionnaire ou du sondage.


Il y aura deux types d'aministrateurs pour gérer le site.
L'admin pourra :
    - se connecter 
    - se déconnecter
    - créer son questionnaire ou son sondage
    - modifier son questionnaire ou son sondage 
    - supprimer son questionnaire ou son sondage
    - voir tous les questionnaires ou sondages de l'application
    - modifier son mot de passe
    - envoyer un questionnaire , sondage via un lien url
    - faire le traitement des résultats

Le super admin aura les mêmes droits que l'admin et pourra en plus :
    - modifier tous les questionnaires ou son sondages
    - supprimer tous questionnaires ou son sondages
    - créer un utilisateur (profil admin ,super admin)
    - modifier un utilisateur (profil admin, super admin)
    - supprimer un utilisateur (profil admin, super admin)

        



